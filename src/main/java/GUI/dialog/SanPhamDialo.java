    private void buttonRadius1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRadius1ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter imageFilter = new FileNameExtensionFilter("hinh anh", "jpg","png");
        fileChooser.setFileFilter(imageFilter);
        
        fileChooser.setMultiSelectionEnabled(false);
        
        int x = fileChooser.showDialog(this, "Choose");
        if(x == JFileChooser.APPROVE_OPTION){
            File f = fileChooser.getSelectedFile();
            
            lb_Img.setIcon(new ImageIcon(f.getAbsolutePath()));

            txt_URLImg.setText(f.getAbsolutePath());
        }
        
    }//GEN-LAST:event_buttonRadius1ActionPerformed

