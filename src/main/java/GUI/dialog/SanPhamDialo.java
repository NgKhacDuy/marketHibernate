    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        // TODO add your handling code here:
        if(status==1){//1 la insert
            Vegetable v = new Vegetable();
            v.setVegetable_Name(txt_NameVegtable.getText());
            v.setAmount(Integer.parseInt(txt_Amount.getText()));
            v.setUnit(cbb_Unit.getSelectedItem().toString());
            listCategory.forEach((p)-> {
                if(p.getName().equals(cbb_Category.getSelectedItem().toString()))
                    v.setCategory(p);
            });
            v.setImage(txt_URLImg.getText());
            v.setPrice(Float.valueOf(txt_Price.getText()));
            vegBLL.newVegetable(v);
        }
        else{//! la update
            Vegetable v = new Vegetable();
            v.setVegetableID(Integer.parseInt(txt_IDVegetable.getText()));
            v.setVegetable_Name(txt_NameVegtable.getText());
            v.setAmount(Integer.parseInt(txt_Amount.getText()));
            v.setUnit(cbb_Unit.getSelectedItem().toString());
            listCategory.forEach((p)-> {
                if(p.getName().equals(cbb_Category.getSelectedItem().toString()))
                    v.setCategory(p);
            });
            v.setImage(txt_URLImg.getText());
            v.setPrice(Float.valueOf(txt_Price.getText()));
            vegBLL.updateVegetable(v);
        }
        this.dispose();
    }//GEN-LAST:event_btn_SaveActionPerformed

